//  Triangle shape
document.getElementById('btn-01a').addEventListener('click', function multiply(){
//      Get the values from the input boxes
        const input01a = document.getElementById("input01a").value;
        const input02a = document.getElementById("input02a").value;
    
//      Multiply the values
        const result = 0.5 * input01a * input02a;
    
//      Set the result in the output box
        document.getElementById("output01a").value = result;
        })

        

